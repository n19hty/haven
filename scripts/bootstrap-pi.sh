#!/bin/bash
# Haven вҖ” Fresh Raspberry Pi Bootstrap
# Takes a clean Raspberry Pi OS install, pulls Haven from GitHub, and gets it
# running as a kiosk gaming console.
#
# Usage (on the Pi):
#   curl -fsSL https://raw.githubusercontent.com/<you>/haven/main/scripts/bootstrap-pi.sh | bash -s -- <repo-url>
# or after copying this file over:
#   bash bootstrap-pi.sh https://github.com/<you>/haven.git
#
# The repo URL can also be supplied via the HAVEN_REPO env var. Install location
# defaults to ~/haven and can be overridden with HAVEN_DIR.

set -euo pipefail

# в”Җв”Җ Config в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
REPO_URL="${1:-${HAVEN_REPO:-}}"
BRANCH="${HAVEN_BRANCH:-main}"
TARGET_DIR="${HAVEN_DIR:-$HOME/haven}"

if [[ -z "$REPO_URL" ]]; then
  echo "ERROR: no repository URL provided."
  echo ""
  echo "  Usage: bash bootstrap-pi.sh <github-repo-url>"
  echo "  e.g.   bash bootstrap-pi.sh https://github.com/youruser/haven.git"
  echo ""
  echo "  (or set the HAVEN_REPO environment variable)"
  exit 1
fi

echo ""
echo "  в–Ҳв–Ҳв•—  в–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—"
echo "  в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘"
echo "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘"
echo "  в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв•қ  в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘"
echo "  в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘"
echo "  в•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ  в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ"
echo ""
echo "  Bootstrap вҖ” Raspberry Pi"
echo "  Repo:   $REPO_URL ($BRANCH)"
echo "  Target: $TARGET_DIR"
echo ""

# в”Җв”Җ System packages в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# git for cloning, curl for the Node setup script, python3 for the server
# runtime, plus the kiosk runtime deps (Chromium browser + unclutter to hide the
# cursor) that a headless image lacks. (Node itself is installed by setup-pi.sh,
# and is only needed to build the client.)
echo "[1/4] Installing system packages (git, curl, python3, chromium, unclutter)..."
sudo apt-get update
sudo apt-get install -y git curl ca-certificates python3 python3-venv python3-pip unclutter

# Chromium package name differs across Raspberry Pi OS releases.
if ! command -v chromium-browser &>/dev/null && ! command -v chromium &>/dev/null; then
  sudo apt-get install -y chromium-browser || sudo apt-get install -y chromium || \
    echo "  ! Could not auto-install Chromium вҖ” install it manually for kiosk mode."
fi

# в”Җв”Җ Clone / update the repo в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [[ -d "$TARGET_DIR/.git" ]]; then
  echo "[2/4] Existing checkout found вҖ” updating..."
  git -C "$TARGET_DIR" fetch --all --prune
  git -C "$TARGET_DIR" checkout "$BRANCH"
  git -C "$TARGET_DIR" pull --ff-only origin "$BRANCH"
else
  echo "[2/4] Cloning Haven into $TARGET_DIR..."
  rm -rf "$TARGET_DIR"
  git clone --branch "$BRANCH" "$REPO_URL" "$TARGET_DIR"
fi

# в”Җв”Җ Hand off to the in-repo setup script в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# setup-pi.sh installs Node 20, runs npm install + build, installs the systemd
# service (haven.service) and the kiosk autostart entry. Keeping it as the single
# source of truth means bootstrap and manual setups stay in sync.
echo "[3/4] Running in-repo setup (Node, build, service, kiosk)..."
cd "$TARGET_DIR"
bash scripts/setup-pi.sh

# в”Җв”Җ Done в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
echo "[4/4] вң“ Haven bootstrapped from GitHub."
echo ""
echo "  Update later with:  cd $TARGET_DIR && git pull && bash scripts/setup-pi.sh"
echo "  Reboot to launch the console in kiosk mode:  sudo reboot"
echo ""
